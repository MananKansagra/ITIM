#include <stdio.h>

/**
 * @brief The main function where program execution begins.
 * * This program demonstrates the simplest form of C output by printing
 * the string "Hello World" to the console.
 *
 * @return 0 on successful execution.
 */
int main(void) {
    // The printf function is used to output formatted text to the standard output (console).
    // The '\n' is a newline character, which moves the cursor to the next line.
    printf("Hello World\n");
    
    // Return 0 indicates successful execution of the program.
    return 0;
}
